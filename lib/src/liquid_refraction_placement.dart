/// 液态折射层相对内容的摆放位置。
///
/// 这里单独拆成枚举，而不是继续往主组件里塞布尔参数，
/// 是因为“直接折射内容”和“放在内容下方”是两种明确的布局语义。
/// 用枚举表达以后，调用方读代码时一眼就能看懂当前页面想要哪一种效果。
enum LiquidRefractionPlacement {
  /// 直接折射内容本身。
  ///
  /// 这是当前仓库最早的使用方式，child 会直接成为 shader 的采样源，
  /// 适合把整个舞台都当成液面来处理。
  content,

  /// 折射层放在内容下方。
  ///
  /// 这一模式更接近“背景效果容器”的心智模型。
  /// child 会保持原样显示在上层，液态层则单独使用 `backdrop` 作为采样源。
  background,
}
